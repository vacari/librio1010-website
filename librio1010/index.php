        const originalText = '‚ñ∂Ô∏è Assista ao v√≠deo';
        
        // Fun√ß√£o para ativar o som
        function activateSound() {
            overlay.style.display = 'none';
            muteBtn.style.display = 'block';
            heroVideo.muted = false;
